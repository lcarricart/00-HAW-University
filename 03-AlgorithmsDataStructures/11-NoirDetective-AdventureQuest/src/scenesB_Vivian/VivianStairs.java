package scenesB_Vivian;

import scenes.NodeScene;

public class VivianStairs extends NodeScene {

	public VivianStairs(String uniqueName) {
		super(uniqueName);
	}

	@Override
	public void start() {
		System.out.println("I push the heavy fire door. It swings open on well-oiled hinges. Finally, some luck.");
        System.out.println("The stairwell is concrete and cold. It smells like wet dust.");
        System.out.println("");
        System.out.println("First floor. Easy. Just the wet sound of my shoes on the stone.");
        System.out.println("Second floor. The ache in my legs wakes up. It starts dim and turns into");
        System.out.println("a scream. My lungs burn. Too many smokes, not enough sleep.");
        System.out.println("");
        System.out.println("I pause on the landing. Leaning against the rail. Gasping. The sound of my own");
        System.out.println("heartbeat is thudding in my ears. Thump. Thump. Thump.");
        System.out.println("");
        System.out.println("But it's quiet. No gears grinding. No bells ringing.");
        System.out.println("I creep up the last few steps to the second floor door. I ease it open just a crack.");
        System.out.println("");
        System.out.println("The hallway is still. No one knows I'm here. My hands are shaking from the climb,");
        System.out.println("but I'm safe. Room 209 is down the hall, drowning in the blinking light of a dying bulb.");
        System.out.println("");
	}
}