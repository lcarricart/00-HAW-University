package scenesA;

import scenes.NodeScene;

public class ApartmentStart extends NodeScene{
	public ApartmentStart(String uniqueName) {
		super(uniqueName);
	}
	
	@Override
	public void start() {
		System.out.println("God, the key is heavy. Why is the key so heavy? Come on... slide in. There. That scrape is too loud.");
		System.out.println("Like a gunshot in the hallway. 4 AM, the whole building is dead, and I’m the only one trying to get back in.");
		System.out.println("");
		System.out.println("(Door open -- Door closed -- Lock it). Finally.");
		System.out.println("");
		System.out.println("The air in here is stale. Smells like dust and old coffee. Doesn't matter. It’s quiet. My legs hurt ");
		System.out.println("with a deep, bone-level ache. Just need to get the shoes off. One foot. There. The floor is cold through");
		System.out.println("the sock. Just get to the bedroom. Don't look at the walls, don't look at the sink.");
		System.out.println("");
		System.out.println("My eyes burn. Rubbing them makes it worse. Just...");
		System.out.println("");
		System.out.println("Wait.");
		System.out.println("What is that?");
		System.out.println("In the corner. On the table.");
		System.out.println("");
		System.out.println("(Telephone dialer light, flickering red and black...)");
		System.out.println("");
		System.out.println("No. Not tonight. Please, not tonight. It cuts right through the dark. A little red eye, opening");
		System.out.println("and closing. Someone called. Someone left a message. At what time? Two? Three? Who calls at three?");
		System.out.println("");
		System.out.println("Blink. It’s pulsing. Like a heartbeat.");
		System.out.println("");
		System.out.println("If I walk over there... if I press that button... the silence is over. Whatever is on that tape becomes real. It");
		System.out.println("becomes a problem. It becomes a name, a voice, a crisis I have to fix while my hands are shaking from fatigue.");
		System.out.println("");
		System.out.println("But if I don't?");
		System.out.println("");
		System.out.println("I’ll lay in bed, staring at the ceiling, and I’ll see that red light pulsing on the back of my eyelids.");
		System.out.println("Who was it? Is it her? Is it them? An emergency?");
		System.out.println("");
		System.out.println("It’s teasing me. Just a lightbulb, holding the whole room hostage.");
		System.out.println("Do I rip it open now? Let the poison out? Or do I let it sit there and rot until sunrise?");
		System.out.println("");
		System.out.println("Just press it. Don't press it. PRESS IT.");
		System.out.println("");
	}
}
